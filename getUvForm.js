function getUV(i) {
  //i -> 0...14

  let w = 0.25;
  //let diff = 0.1 / 2;
  let diff = 0.00625; //for 10:0.05 = 0.25:X => x = 0.05*0.25/10;

  let col = i%4;
  let row = parseInt(i/4);

  let x0, x1;
  let y0, y1;
  //set 5 diff points for each direciton

  if (col === 0) {
    x0 = 0;
    x1 = w - diff;
  }
  else if (col === 1) {
    x0 = w + diff;
    x1 = 2*w - diff;
  }
  else if (col === 2) {
    x0 = 2*w + diff;
    x1 = 3*w - diff;
  } else if (col === 3) {
    x0 = 3*w + diff;
    x1 = 1;
  }

  if (row === 0) {
    y0 = 1;
    y1 = 3*w + diff;
  }
  else if (row === 1) {
    y0 = 3*w - diff;
    y1 = 2*w + diff;
  }
  else if (row === 2) {
    y0 = 2*w - diff;
    y1 = w + diff;
  } else if (row === 3) {
    y0 = w - diff;
    y1 = 0;
  }


  // let a = [1,2];
  // let b = [3,4];
  // let c = [5,6];
  // let d = [7,8];
  // let x = [...a,...b,...c,...d];

  //0 [0, 1, 0.25, 1, 0, 0.75, 0.25, 0.75],
  return [x0, y0, x1, y0, x0, y1, x1, y1];


}

